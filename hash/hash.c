#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>


//*****************************************************************************************************
int main(int argc, char *argv[]){
	if(argc == 2){
		char *name=argv[1];
		int c = strlen(name);
		int hash = 0;
		for(int i=0; i<=c; i++){
			hash += name[i];
			hash = hash % 59;
		}
		fprintf(stdout, "%d\n", hash);
	}
	
	return EXIT_SUCCESS;
}
